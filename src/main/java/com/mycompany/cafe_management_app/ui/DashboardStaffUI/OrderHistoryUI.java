    public OrderHistoryUI(Bill bill) {
        initComponents();
        this.bill = bill;
      
        String formatDateTime = bill.getTimeCreated().format(formatter);
        timeLabel.setText(formatDateTime.toString());
        // format bill.getTotalPrice().toString() th√†nh %,.0f

        String totalPrice = String.format("%,.0f", bill.getTotalPrice());
        String receiveAmount = String.format("%,.0f", bill.getReceivedAmount());
        String returnAmount = String.format("%,.0f", bill.getReturnedAmount());


        totalPriceLabel.setText(totalPrice);
        receiveAmountLabel.setText(receiveAmount);
        returnAmountLabel.setText(returnAmount);
        paymentMethodLabel.setText(bill.getPaymentMethod());
    }

